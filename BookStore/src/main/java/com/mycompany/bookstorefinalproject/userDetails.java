    void findMaxUserID(){
        String result;
        int userID = 0;
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/BookStoreDB", "test", "123");
            psQuery = con.prepareStatement("SELECT MAX(USERID) FROM USERDATA");
            ResultSet queryResult = psQuery.executeQuery();
            while (queryResult.next()) {
                result = queryResult.getString(1);
                if (result == null) {
                    userID = 1;
                } else {
                    userID = Integer.parseInt(result) + 1;
                }
            }
            jTextField1.setText("" + userID);

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
