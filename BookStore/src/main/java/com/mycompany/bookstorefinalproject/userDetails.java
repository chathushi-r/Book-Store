    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        Vector v = (Vector)jComboBox4.getSelectedItem();
        try {
            con=DriverManager.getConnection("jdbc:derby://localhost:1527/BookStoreDB","test","123");
            psQuery=con.prepareStatement("UPDATE USERDATA SET USERNAME=?,PASSWORD=?,USERROLE=? WHERE USERID=?");
            psQuery.setString(1,jTextField4.getText());
            if(String.valueOf(jPasswordField3.getPassword()).equals(String.valueOf(jPasswordField4.getPassword())))
            {
                psQuery.setString(2,String.valueOf(jPasswordField3.getPassword()));
                if(jRadioButton8.isSelected()){
                    psQuery.setString(3,"admin");
                }else if(jRadioButton2.isSelected()){
                    psQuery.setString(3,"user");
                }
                psQuery.setInt(4,Integer.parseInt((String)v.get(0)));
                int confirmOpt = JOptionPane.showConfirmDialog(rootPane, "Do you want to update current user details?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if(confirmOpt == JOptionPane.YES_OPTION){
                    psQuery.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "User details updated.");
                    createFile("User Id - "+(String)v.get(0)+" updated\n");
                    reset();
                    jButton2ActionPerformed(evt);
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Password fields do not match. Please try again.");
                reset();
            }
           
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton15ActionPerformed

