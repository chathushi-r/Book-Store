    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        try {
            con=DriverManager.getConnection("jdbc:derby://localhost:1527/BookStoreDB","test","123");
            psQuery=con.prepareStatement("INSERT into USERDATA(USERID,USERNAME,PASSWORD,USERROLE) values(?,?,?,?)");
            psQuery.setInt(1,Integer.parseInt(jTextField1.getText()));
            psQuery.setString(2,jTextField2.getText());
            if(String.valueOf(jPasswordField1.getPassword()).equals(String.valueOf(jPasswordField2.getPassword())))
            {
                psQuery.setString(3,String.valueOf(jPasswordField1.getPassword()));
                if(jRadioButton7.isSelected()){
                    psQuery.setString(4,"admin");
                }else if(jRadioButton1.isSelected()){
                    psQuery.setString(4,"user");
                }
                int confirmOpt = JOptionPane.showConfirmDialog(rootPane, "Do you want to add new user?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if(confirmOpt == JOptionPane.YES_OPTION)
                {
                    psQuery.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "New user added.");
                    createFile("New user added. User Id - "+jTextField1.getText()+"\n");
                    reset();
                    jButton1ActionPerformed(evt);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Password fields do not match. Please try again.");
                reset();
            }   
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton11ActionPerformed

